# collections
# str, list, dict, tuple, range, set

# list: heterogeneous mutable sequence

s = "This is a list of words in a sequence"

s = s.split()

print(s)

print(s[4])

print(s[-5])

print(s[1: 4])

# all elements except first and last

print(s[1:-1])

print(s[3:])

print(s[:2])

# copying a list

s2 = s[:]

print(s2 is s)

